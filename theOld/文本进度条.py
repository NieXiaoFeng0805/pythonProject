import time
scale = 10 #文本进度条的宽度
print("--------执行开始---------")
for i in range(scale+1):
    a = '*' * i     #表示*被复制的次数（指当前的百分比所表示的信息）#
    b = '.' * (scale-i)     #表示剩余进度条的比例之间的乘积#
    #构成当前比例中 已经执行的和未知执行的之间的对应的关系#
    c = (i/scale)*100   #输出与当前进度条和进度相关的百分比
    print("{:^3.0f}%[{}->{}]".format(c,a,b))    
    time.sleep(0.1)     
print("-----------执行结束------------")
